public class Dokter {
    private String name;
    private String spesialis;
     
    public Dokter(String name, String spesialis) {
        this.name = name;
        this.spesialis = spesialis;
    }
 
    public String getName() {
        return name;
    }

    public String getSpesialis() {
        return spesialis;
    }
}
